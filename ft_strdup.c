#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s;
	int		len_s;

	len_s = ft_strlen(s1);
	s = (char *)malloc(len_s + 1);
	if (!s)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return ((char *)s);
}
